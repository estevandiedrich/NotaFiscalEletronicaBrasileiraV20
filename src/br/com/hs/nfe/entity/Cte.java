package br.com.hs.nfe.entity;

import java.io.Serializable;
import java.util.Date;

public class Cte implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = -2860699466125727049L;
	
	private String chaveAcesso;
	private String nprot;
	private String cstat;
	private String xmotivo;
	private String cnpj;
	private Date dhRecbto;
	private int serie;
    private int nct;
    private String cuf;
    private String dest;
    private String rem;
    private Character tpAmb;
    private Character tpEmis;
    private String digVal;
    private Character dacteGerado;
    public Cte()
    {
    	
    }
	public Cte(String chaveAcesso, String nprot, String cstat, String xmotivo,
			String cnpj, Date dhRecbto, int serie, int nct, String cuf,
			String dest, Character tpAmb, Character tpEmis, String digVal,
			Character dacteGerado,String rem) {
		super();
		this.chaveAcesso = chaveAcesso;
		this.nprot = nprot;
		this.cstat = cstat;
		this.xmotivo = xmotivo;
		this.cnpj = cnpj;
		this.dhRecbto = dhRecbto;
		this.serie = serie;
		this.nct = nct;
		this.cuf = cuf;
		this.dest = dest;
		this.tpAmb = tpAmb;
		this.tpEmis = tpEmis;
		this.digVal = digVal;
		this.dacteGerado = dacteGerado;
		this.rem = rem;
	}
	public String getChaveAcesso() {
		return chaveAcesso;
	}
	public void setChaveAcesso(String chaveAcesso) {
		this.chaveAcesso = chaveAcesso;
	}
	public String getNprot() {
		return nprot;
	}
	public void setNprot(String nprot) {
		this.nprot = nprot;
	}
	public String getCstat() {
		return cstat;
	}
	public void setCstat(String cstat) {
		this.cstat = cstat;
	}
	public String getXmotivo() {
		return xmotivo;
	}
	public void setXmotivo(String xmotivo) {
		this.xmotivo = xmotivo;
	}
	public String getCnpj() {
		return cnpj;
	}
	public void setCnpj(String cnpj) {
		this.cnpj = cnpj;
	}
	public Date getDhRecbto() {
		return dhRecbto;
	}
	public void setDhRecbto(Date dhRecbto) {
		this.dhRecbto = dhRecbto;
	}
	public int getSerie() {
		return serie;
	}
	public void setSerie(int serie) {
		this.serie = serie;
	}
	public int getNct() {
		return nct;
	}
	public void setNct(int nct) {
		this.nct = nct;
	}
	public String getCuf() {
		return cuf;
	}
	public void setCuf(String cuf) {
		this.cuf = cuf;
	}
	public String getDest() {
		return dest;
	}
	public void setDest(String dest) {
		this.dest = dest;
	}
	public Character getTpAmb() {
		return tpAmb;
	}
	public void setTpAmb(Character tpAmb) {
		this.tpAmb = tpAmb;
	}
	public Character getTpEmis() {
		return tpEmis;
	}
	public void setTpEmis(Character tpEmis) {
		this.tpEmis = tpEmis;
	}
	public String getDigVal() {
		return digVal;
	}
	public void setDigVal(String digVal) {
		this.digVal = digVal;
	}
	public Character getDacteGerado() {
		return dacteGerado;
	}
	public void setDacteGerado(Character dacteGerado) {
		this.dacteGerado = dacteGerado;
	}
	public String getRem() {
		return rem;
	}
	public void setRem(String rem) {
		this.rem = rem;
	}
	
}
